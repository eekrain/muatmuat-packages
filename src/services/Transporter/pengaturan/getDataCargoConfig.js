export const fetcherTransporterCargoData = async (cacheKey) => {
  if (useMockData) {
    return mockApiResult.data.Data;
  }
  // The try/catch block was removed because it was redundant.
  // If fetcherMuatrans.get() rejects, the async function will
  // automatically propagate the rejection.
  const result = await fetcherMuatrans.get(cacheKey);
  return result?.data?.Data || {};
};

export const useGetTransporterCargoData = (id, params = {}, options = {}) => {
  const queryParams = new URLSearchParams(params).toString();

  const cacheKey = id
    ? `/v1/transporter-cargo-config/${id}${
        queryParams ? `?${queryParams}` : ""
      }`
    : null;

  return useSWR(cacheKey, fetcherTransporterCargoData, options);
};
